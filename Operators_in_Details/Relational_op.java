package java_Operators;

public class Relational_op {

	public static void main(String[] args) {
	    //	 Relational Operator / Comparison Operator ::  ==, !=, <, >, <=, >=
		//         Used to checked for relations between two operands like equality,greater than and less than.
		//         They return boolean results after the comparison.

		int num = 23, num1 = 54 , num2 = 43;
       
		System.out.println("Num == Num1 :: " + (num == num1));
		System.out.println("Num1 != Num2 :: " + (num1 != num2));
		System.out.println("Num2 < Num1 :: " + (num2 < num1));
		System.out.println("Num > Num2 :: " + (num > num2));
		System.out.println("Num1 <= Num :: " + (num1 <= num));
		System.out.println("Num2 >= Num1 :: " + (num2 >= num1));
		
		
	
		
	}

}
